package main

import "fmt"

func main() {
	/**
	 * Strings
	 */
	var firstName string = "Talha"
	var secName = "Yaseen"

	fmt.Println(firstName, secName)

	firstName = "Abhinav"
	secName = "Singh"
	fmt.Println(firstName, secName)

	/**
	 * Integers
	 */
	var num1 int = 20
	var num2 = 34
	num3 := 22
	fmt.Println(num1, num2, num3)

	/**
	 * Floats
	 */
	var decNum1 float32 = 34.098
	var decNum2 float64 = 8585933085669695.0839849
	decNum3 := 1.2
	fmt.Println(decNum1, decNum2, decNum3)
}
